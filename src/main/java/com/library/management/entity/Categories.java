package com.library.management.entity;


public class Categories {

	private Integer id;
	private String name;

	public Integer getId() {
		return id;
	}

	public Categories setId(Integer id) {
		this.id = id;
		return this;
	}

	public String getName() {
		return name;
	}

	public Categories setName(String name) {
		this.name = name;
		return this;
	}
}

